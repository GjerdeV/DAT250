class User:
    def __init__(self, name, lastname, email, userid, password, phone, dob, city, zip, address):
        self.name = name
        self.lastname = lastname
        self.email = email
        self.userid = userid
        self.password = password
        self.phone = phone
        self.dob = dob
        self.city = city
        self.zip = zip
        self.address = address


